    private void ClicSurBouton(String nombouton)
    {
        
        int pos = nombouton.indexOf('_');
        int i = Integer.parseInt(nombouton.substring(6, pos));
        int j = Integer.parseInt(nombouton.substring(pos+1, nombouton.length()));
        nbCoups++;
        jTextFieldNBCOUPS.setText( Integer.toString( nbCoups));
        partieEnCours.getPlateau().clic(i, j);
        
        MiseAJourAffichage();
    }
    
        
    private void MiseAJourAffichage()
    {
        int i,j;
               
        for(i=0;i<(Integer) jSpinnerTailleGrille.getValue();i++)
        {
            for(j=0;j<(Integer) jSpinnerTailleGrille.getValue();j++)
            {
               if (partieEnCours.getPlateau().getCellules()[i][j].est_allume())
               {
                  //  jTableGrille.
                  ListeBouton[i][j].setText("1");
                  ListeBouton[i][j].setBackground(Color.yellow);
 
                }
                else
                {
                  ListeBouton[i][j].setText("0");
                  ListeBouton[i][j].setBackground(Color.GRAY);
                }
            }
        }
    }
    
    
