    private void jButtonNouvellePartieMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonNouvellePartieMouseClicked
        // on créée la partie avec le paramétrage demandé
        partieEnCours = new Partie();
        partieEnCours.placerCase((int) jSpinnerTailleGrille.getValue());
        nbCoups = 0;
        jTextFieldNBCOUPS.setText( Integer.toString( nbCoups));
        // on créé les bouttons à l'écran
        ListeBouton = new JButton[(int) jSpinnerTailleGrille.getValue()][(int) jSpinnerTailleGrille.getValue()];
        int i,j;        
        InternalFrame.setLayout(new GridLayout((Integer) jSpinnerTailleGrille.getValue(),(Integer) jSpinnerTailleGrille.getValue()));
        InternalFrame.getContentPane().removeAll();
        InternalFrame.repaint();
        for(i=0;i<(Integer) jSpinnerTailleGrille.getValue();i++)
        {
            for(j=0;j<(Integer) jSpinnerTailleGrille.getValue();j++)
            {
		// Creates button with text 'Test Button'
		JButton button = new JButton("Test Button");
                button.setName("BUTTON"+i+"_"+j);
		button.setSize(30,30);
                //button.addActionListener(this);
                 button.addActionListener(new ActionListener()    {  public void actionPerformed(ActionEvent e) { JButton currentButton = (JButton) e.getSource(); ClicSurBouton(currentButton.getName()); }           });                
		//Creates a container which is going to have various controls like button.. 
		Container container = InternalFrame.getContentPane();
                ListeBouton[i][j] = button;
		// Adding button to container of frame.
		container.add(button);
		InternalFrame.setSize(500,200);
		InternalFrame.setVisible(true);
		//frame.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
            
          if (partieEnCours.getPlateau().getCellules()[i][j].est_allume())
          {
            //  jTableGrille.
              button.setText("1");
              button.setBackground(Color.yellow);
 
          }
          else
          {
              button.setText("0");
              button.setBackground(Color.GRAY);
          }
        }
      }
          
          
                 
    }//GEN-LAST:event_jButtonNouvellePartieMouseClicked

